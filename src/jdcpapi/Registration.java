    private void userRegister(String fname, String lname, String Email, String add, String uname, String passwordVal) {
       Connection dbconn = DBConnection.connectBD();
        if(dbconn != null){
        try {
            PreparedStatement stmt = (PreparedStatement)
            dbconn.prepareStatement("INSERT INTO SIGNUP (firstname, lastname, email, address, username, password) VALUES(?, ?, ?, ?, ?, ?)");
            stmt.setString(1, fname);
            stmt.setString(2, lname);
            stmt.setString(3, Email);
            stmt.setString(4, add);
            stmt.setString(5, uname);
            stmt.setString(6, passwordVal);
       
            int repol = stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully Added..!", "Success", JOptionPane.INFORMATION_MESSAGE);
            
          
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            System.out.println("The connection is not avaliable.");
        }
    }
