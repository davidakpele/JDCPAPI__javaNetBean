    private void RegiterSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegiterSubmitBtnActionPerformed
        // TODO add your handling code here:
        String fname = firstname.getText();
        String lname = lastname.getText();
        String Email = Useremail.getText();
        String add = address.getText();
        String uname = username.getText();
        String gender = Gender.getText();
        String passwordVal = String.valueOf(password.getPassword());
        
         if(fname.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your First-Name.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if(lname.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Last-Name", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if(Email.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Email Address.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if(add.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Address Details.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if(uname.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Username.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if(gender.isEmpty()){
            JOptionPane.showMessageDialog(this, "Select Your Gender", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if(passwordVal.isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Your Password.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            //start login process
            userRegister(fname, lname, Email, add, uname, passwordVal);
        }
    }//GEN-LAST:event_RegiterSubmitBtnActionPerformed

